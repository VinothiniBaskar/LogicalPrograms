package string.programs;

public class Alphaphet {

	public static void main(String[] args) {
		char ch='h';
		
		if (ch>='a'&&ch<='z'||ch>='A'&&ch<='Z') {
			System.out.println("Alphaphet Character");
		}
		else {
			System.out.println(" not Alphaphet Character");

		}
		

	}

}
